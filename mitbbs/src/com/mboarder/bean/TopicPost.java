public class TopicPost {
	
	private String user;
	private String date;
	private String msg;
	
	public TopicPost(String user,String date,String msg)
	{
		this.user = user;
		this.date = date;
		this.msg = msg;
	}

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getUser() {
		return user;
	}

	public void setUser(String user) {
		this.user = user;
	}

	public String getMsg() {
		return msg;
	}

	public void setMsg(String msg) {
		this.msg = msg;
	}

}
